struct stats_vars calc_stats(double *dVec, int nVals){
	
	struct stats_vars retur;
	double **temp = &dVec;
	
	retur.vectorSize = nVals;
	retur.avg = 0.0;
	retur.min = 0.0;
	retur.max = 0.0;
	retur.var = 0.0;
	
	
	//Determines the maximum value of the vector
	retur.max = *dVec;
	int i=0;
	for(; i < (nVals-1); i++){
		
		if(retur.max < *(dVec+1)){
			
			retur.max = *(dVec+1);
			
		}
		
		*dVec++;
		
	}
	
	dVec = *temp;
	//Determines the minimum value of the vector
	retur.min = *dVec;
	i=0;
	
	for(; i < (nVals-1); i++){
		
		if(retur.min > *(dVec+1)){
			
			retur.min = *(dVec+1);
			
		}
		*dVec++;
		
	}
	
	dVec = *temp;
	
	
	//Determines the average value of the vector
	retur.avg = 0.0;
	i=0;
	for(; i < nVals; i++){
		
		retur.avg = retur.avg + *dVec;
		*dVec++;
	}
	
	retur.avg = retur.avg / nVals;
	
	
	dVec = *temp;
	
	//Determines the variance
	i=0;
	for(; i < nVals; i++){
		
		retur.var = retur.var + ((*dVec - retur.avg)*(*dVec - retur.avg));
		*dVec++;
		
		
	}
	
	retur.var = retur.var / nVals;
	
	
	return retur;
	
}

